package semana2;

public class concatenacao {
    public static void main (String[] args){
        String concatencao = "?";
        System.out.println(concatencao);
        concatencao = 1+1+1+"1";
        System.out.println(concatencao);
        concatencao = 1+"1"+1+1;
        System.out.println(concatencao);
        concatencao = 1+"1"+1+"1";
        System.out.println(concatencao);
        concatencao = "1"+1+1+1;
        System.out.println(concatencao);
        concatencao = "1" + (1+1+1);
        System.out.println(concatencao);
    }
}
